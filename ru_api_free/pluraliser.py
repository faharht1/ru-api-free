def _get_gender(word):
    w = word.lower().strip()
    if w.endswith("а") or w.endswith("я"):
        if w.endswith("мя"):
            return "n"
        return "f"
    if w.endswith("о") or w.endswith("е") or w.endswith("ё"):
        return "n"
    if w in MASCULINE_SOFT:
        return "m"
    if w in FEMININE_SOFT:
        return "f"
    if w.endswith("ь"):
        if w.endswith("ость") or w.endswith("есть") or w.endswith("ознь"):
            return "f"
        if w.endswith("чь") or w.endswith("шь") or w.endswith("щь") or w.endswith("жь"):
            return "f"
        return "m"
    if w[-1].isalpha():
        return "m"
    return "m"


def _is_verb_like(word):
    if word.endswith("чь") and len(word) > 3:
        return word[:-3] in ("мо", "пе", "те", "се", "же", "ле",
                             "сте", "бе", "вле", "помо", "запе")
    if word.endswith("сть") or word.endswith("знь") or word.endswith("зть"):
        return False
    if word.endswith("ть") and len(word) > 3:
        before = word[-3]
        is_verb = before in "аеёиоуыэюя"
        return is_verb
    return word.endswith("ти") or word.endswith("зти")


def _last_is_sibilant(word):
    return word and word[-1] in "жшщч"


def _last_is_velar(word):
    return word and word[-1] in "гкх"


def pluralise(word):
    w = word.lower().strip()
    if w in IRREGULAR_PLURALS:
        return IRREGULAR_PLURALS[w]

    if _is_verb_like(w):
        return None

    gender = _get_gender(w)

    if w.endswith("ёнок") or w.endswith("енок") or w.endswith("онок"):
        stem = w[:-4] if (w.endswith("ёнок") or w.endswith("енок")) else w[:-4]
        return stem + "ята" if w.endswith("онок") else stem + "ята"

    if gender == "m":
        if _last_is_sibilant(w):
            return w + "и"
        if _last_is_velar(w):
            return w + "и"
        if w in STRESSED_A_MASC:
            return w[:-1] + "а" if w.endswith("ь") or w.endswith("й") else w + "а"
        if w.endswith("й"):
            return w[:-1] + "и"
        if w.endswith("ок") and len(w) > 2:
            stem = w[:-2]
            if _last_is_velar(stem) or _last_is_sibilant(stem):
                return stem + "и"
            return stem + "ки"
        if w.endswith("ец"):
            return w[:-2] + "цы"
        if w.endswith("ин") and len(w) > 3 and w not in ("сын",):
            return w[:-2] + "е"
        if w.endswith("ь"):
            return w[:-1] + "и"
        return w + "ы"

    if gender == "f":
        if w.endswith("а") and len(w) > 1:
            stem = w[:-1]
            if _last_is_velar(stem) or _last_is_sibilant(stem):
                return stem + "и"
            if stem.endswith("ц"):
                return stem + "ы"
            return stem + "ы"
        if w.endswith("я"):
            if w.endswith("ия"):
                return w[:-1] + "и"
            stem = w[:-1]
            if _last_is_velar(stem) or _last_is_sibilant(stem):
                return stem + "и"
            return w[:-1] + "и"
        if w.endswith("ь"):
            return w[:-1] + "и"
        return w + "ы"

    if gender == "n":
        if w.endswith("о"):
            return w[:-1] + "а"
        if w.endswith("е") or w.endswith("ё"):
            stem = w[:-1]
            if _last_is_sibilant(stem):
                return stem + "и"
            if w.endswith("ие"):
                return w[:-2] + "ия"
            if w.endswith("ье"):
                s = w[:-2]
                if _last_is_sibilant(s):
                    return w[:-1] + "а"
                return w[:-1] + "я"
            return w[:-1] + "я"
        if w.endswith("мя"):
            return w[:-2] + "мена"

    return w + "ы"


def pluralise_with_info(word):
    result = pluralise(word)
    if result is None:
        return {"singular": word, "plural": None, "note": "This is a verb, not a noun"}
    gender_map = {"m": "masculine", "f": "feminine", "n": "neuter"}
    info = {
        "singular": word,
        "plural": result,
        "gender": gender_map.get(_get_gender(word), "unknown"),
    }
    if word in IRREGULAR_PLURALS:
        info["exception"] = "irregular"
    return info
