VERBS = {}

def conj2_stem(verb):
    if verb.endswith("ить"):
        return verb[:-3]
    if verb.endswith("еть"):
        return verb[:-3]
    if verb.endswith("ать"):
        return verb[:-3]
    return verb[:-2]

def add(verb, conj, aspect, refl=False, stem=None, mut_1sg=None):
    entry = {"conj": conj, "aspect": aspect, "refl": refl}
    if stem:
        entry["stem"] = stem
    if mut_1sg:
        entry["mut_1sg"] = mut_1sg
    VERBS[verb] = entry

